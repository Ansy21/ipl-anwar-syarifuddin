package Modul5.Tugas.TugasDefensiveProgramming;

public class Tugas1 {
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int divisor = 0;  // Misalnya, nilai pembagi
        int numerator = 3;

        if (divisor != 0) {
            System.out.println(numerator / divisor);
        } else {
            System.out.println("Error: Pembagi tidak boleh nol.");
        }
    }
}